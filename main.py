def run_test():
    theme_key = st.session_state.current_theme
    test_key = st.session_state.current_test
    test = THEMES[theme_key]["tests"][test_key]
    total_questions = len(test["questions"])
    current_q = st.session_state.current_question

    # –ó–∞—â–∏—Ç–∞ –æ—Ç –≤—ã—Ö–æ–¥–∞ –∑–∞ –≥—Ä–∞–Ω–∏—Ü—ã
    if current_q < 0:
        st.session_state.current_question = 0
        st.rerun()
    elif current_q >= total_questions:
        st.session_state.current_question = total_questions - 1
        st.rerun()

    st.title(test["title"])
    st.progress(current_q / total_questions)

    question = test["questions"][current_q]

    st.markdown(f"### –í–æ–ø—Ä–æ—Å {current_q + 1} –∏–∑ {total_questions}")
    st.markdown(f"**{question['text']}**")

    # –í–æ—Å—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –æ—Ç–≤–µ—Ç
    if len(st.session_state.answers) > current_q:
        prev_answer = st.session_state.answers[current_q]
        selected_index = question["options"].index(prev_answer)
    else:
        selected_index = 0

    answer = st.radio(
        "–í—ã–±–µ—Ä–∏—Ç–µ –æ—Ç–≤–µ—Ç:",
        question["options"],
        index=selected_index,
        key=f"q{current_q}"
    )

    st.markdown("<br><br>", unsafe_allow_html=True)

    col1, col2 = st.columns([1, 1])

    # –ö–Ω–æ–ø–∫–∞ "–ù–∞–∑–∞–¥"
    if current_q > 0:
        with col1:
            if st.button("‚Üê –ù–∞–∑–∞–¥", key=f"btn_back_{current_q}", use_container_width=True):
                st.session_state.answers.pop()
                st.session_state.scores.pop()
                st.session_state.current_question -= 1
                st.rerun()

    # –ö–Ω–æ–ø–∫–∞ "–î–∞–ª–µ–µ" –∏–ª–∏ "–ó–∞–≤–µ—Ä—à–∏—Ç—å"
    with col2:
        if current_q == total_questions - 1:
            btn_text = "‚úÖ –ó–∞–≤–µ—Ä—à–∏—Ç—å —Ç–µ—Å—Ç"
        else:
            btn_text = "‚Üí –°–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å"

        if st.button(btn_text, key=f"btn_next_{current_q}", use_container_width=True):
            score_index = question["options"].index(answer)
            if len(st.session_state.answers) > current_q:
                st.session_state.answers[current_q] = answer
                st.session_state.scores[current_q] = question["scores"][score_index]
            else:
                st.session_state.answers.append(answer)
                st.session_state.scores.append(question["scores"][score_index])

            # ‚ö†Ô∏è –ù–µ —É–≤–µ–ª–∏—á–∏–≤–∞–µ–º current_question, –µ—Å–ª–∏ —ç—Ç–æ –ø–æ—Å–ª–µ–¥–Ω–∏–π –≤–æ–ø—Ä–æ—Å
            if current_q == total_questions - 1:
                # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞–º ‚Äî –Ω–µ —Ä–µ—Ä–∞–Ω, –∞ –∏–∑–º–µ–Ω—è–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ
                st.session_state.current_question = total_questions  # –£—Å–ª–æ–≤–Ω–æ "–∑–∞–≤–µ—Ä—à–µ–Ω–æ"
                st.rerun()
            else:
                st.session_state.current_question += 1
                st.rerun()

